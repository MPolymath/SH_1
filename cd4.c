#include "ft_minishell1.h"

void		end_str(char **path, int k)
{
	if ((*path)[k - 1] == '/')
	{
		(*path)[k] = '\0';
		(*path)[k + 1] = '\0';
	}
	else
	{
		(*path)[k] = '/';
		(*path)[k + 1] = '\0';
	}
}
