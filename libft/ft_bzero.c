#include <string.h>

void	ft_bzero(void *b, size_t n)
{
	char	*str;
	size_t	i;

	if (n > 0)
	{
		str = (char *)b;
		i = 0;
		while (i < n)
		{
			str[i] = 0;
			i++;
		}
	}
}
